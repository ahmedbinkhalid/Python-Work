#  Starting Tuple with a Basic Example
professions_in_the_industry = ('Front-end', 'Back-end', 'Full-stack', 'dev_op')
print(type(professions_in_the_industry))
# Accessing cell by Index
print(professions_in_the_industry[0])

# Accessing cell by negative Index
print(professions_in_the_industry[-4])

# Accessing Several cells by range of Indexes
print(professions_in_the_industry[0:3])

# Printing the length
print(len(professions_in_the_industry))
