# Collection By List
list_of_random_values = [31, 4, 31.5, 3, "Yes"]

# Accessing the list by index
print(list_of_random_values[0])
print(list_of_random_values[1])
print(list_of_random_values[2])
print(list_of_random_values[3])
print(list_of_random_values[4])

# If we access the values above index 4 it will give IndexError


# Accessing the list by Negative Index

print(list_of_random_values[-5])
print(list_of_random_values[-4])
print(list_of_random_values[-3])
print(list_of_random_values[-2])
print(list_of_random_values[-1])

# If we access the values above -5 it will give IndexError


