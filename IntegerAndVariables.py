# Integer and Float Variables (Numbers)

# print of a simple string variables
numeric_variable = 10
print("Integer variable value is : " + str(numeric_variable))

# Define simple variables for further manipulations
simple_variable_a = 5
simple_variable_b = 10

# summery of 2 variables
summery_result = simple_variable_a + simple_variable_b
print("The summery result is : " + str(summery_result))

# multiply of 2 variables
multiply_result = simple_variable_a * simple_variable_b
print("The multiply_result is : " + str(multiply_result))

# division of 2 numbers
division_result = simple_variable_a / simple_variable_b
print("The division_result is : " + str(division_result))

# power variable
power_result = simple_variable_a ** 2
print("The power_result is : " + str(power_result))

# Modulo action on 2 variables
modulo_result = simple_variable_b % simple_variable_a
print("The modulo_result is : " + str(modulo_result))

# Decimal point variable -> Float
float_variable_a = 2.5
float_variable_b = 3.0

summery_of_floats = float_variable_b + float_variable_b
print("The summery_of_floats is : " + str(summery_of_floats))

# Apply math action upon a variable
math_action_variable = 5*2
print("The math_action_variable result is : " + str(math_action_variable))
